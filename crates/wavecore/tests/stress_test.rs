// Stress tests for WaveRunner SessionManager and DSP pipeline.
//
// Exercises internals hard to find crashes, panics, data corruption,
// and deadlocks under adversarial conditions. Each test is independent
// and uses ReplayDevice (no hardware required).
//
// FINDINGS:
// - BUG FOUND (Test 13b): Event::AnnotationAdded uses blocking evt_tx.send()
//   in manager.rs. When many AddAnnotation commands are sent, the event
//   channel (bounded 256) fills up, blocking the processing thread. This
//   prevents command consumption, creating a deadlock when the command
//   channel (bounded 64) is also full. Same pattern affects any blocking
//   send event generated by a command (Status events from SetGain, Tune,
//   EnableDecoder, etc).
//
// Run with: cargo test -p wavecore --test stress_test -- --nocapture

