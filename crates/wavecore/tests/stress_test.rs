// ============================================================================
// Test 4: Command flooding (with concurrent event drain)
// ============================================================================

#[test]
fn stress_command_flooding() {
    eprintln!("\n=== Test 4: Command flooding ===");
    let sample_rate = 2_048_000.0;
    let (path, _) = generate_test_cf32("cmdflood", sample_rate, 3.0);

    let device = ReplayDevice::open(&path, sample_rate).expect("open replay device");
    let config = make_config(sample_rate, 2048);

    let (session, event_rx) =
        SessionManager::new_with_device(config, device, full_registry()).expect("start session");

    let rec_path = PathBuf::from(format!(
        "/tmp/waverunner_stress_rec_{}.cf32",
        std::process::id()
    ));

    // MUST drain events concurrently: commands produce blocking Status events
    // which fill the event channel and deadlock if not drained.
    let stop = Arc::new(AtomicBool::new(false));
    let (drainer, drainer_stats) = spawn_event_drainer(event_rx, Arc::clone(&stop));

    // Fire every command type rapidly
    let mut sent = 0u64;
    for i in 0..50u64 {
        let cmds: Vec<Command> = vec![
            Command::Tune(88e6 + (i as f64 * 1e6)),
            Command::SetGain(if i % 2 == 0 {
                GainMode::Auto
            } else {
                GainMode::Manual(20.0 + i as f64)
            }),
            Command::SetSampleRate(if i % 3 == 0 { 1_024_000.0 } else { 2_048_000.0 }),
            Command::EnableDecoder("pocsag".to_string()),
            Command::DisableDecoder("pocsag".to_string()),
            Command::EnableDecoder("adsb".to_string()),
            Command::CaptureReference,
            Command::StartTracking,
            Command::RunAnalysis {
                id: i,
                request: analysis::AnalysisRequest::MeasureSignal(
                    analysis::measurement::MeasureConfig {
                        signal_center_bin: 512,
                        signal_width_bins: 100,
                        adjacent_width_bins: 50,
                        obw_threshold_db: 26.0,
                    },
                ),
            },
            Command::StopTracking,
            Command::AddAnnotation {
                kind: "note".to_string(),
                text: format!("stress annotation {i}"),
            },
        ];

        for cmd in cmds {
            if session.send(cmd).is_err() {
                eprintln!("  Command channel closed after {} commands", sent);
                break;
            }
            sent += 1;
        }
    }

    // Also try start/stop recording
    let _ = session.send(Command::StartRecord {
        path: rec_path.clone(),
        format: RecordFormat::RawCf32,
    });
    std::thread::sleep(Duration::from_millis(100));
    let _ = session.send(Command::StopRecord);

    eprintln!("  Sent {} commands total", sent);

    // Wait for session end
    let deadline = Instant::now() + Duration::from_secs(15);
    while session.is_running() && Instant::now() < deadline {
        std::thread::sleep(Duration::from_millis(100));
    }

    stop.store(true, Ordering::Relaxed);
    session.shutdown();
    drainer.join().expect("drainer panicked");
    std::fs::remove_file(&path).ok();
    std::fs::remove_file(&rec_path).ok();
    let mut sidecar = rec_path.clone();
    sidecar.set_extension("sigmf-meta");
    std::fs::remove_file(&sidecar).ok();

    let stats = drainer_stats.lock().unwrap().clone();
    if let Some(stats) = stats {
        eprintln!(
            "  blocks_processed={}, blocks_dropped={}, events_dropped={}",
            stats.blocks_processed, stats.blocks_dropped, stats.events_dropped
        );
    }
    eprintln!("  PASSED: Command flooding without panic or deadlock");
}

