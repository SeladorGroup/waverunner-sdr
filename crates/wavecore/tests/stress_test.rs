// ============================================================================
// Test 13b: Annotation deadlock detection
//
// BUG: Event::AnnotationAdded uses blocking evt_tx.send() in manager.rs.
// When we send >256 AddAnnotation commands WITHOUT draining events, the
// event channel fills up, the processing thread blocks on send, the command
// channel fills up, and the caller blocks on command send. Deadlock.
//
// This test verifies the bug exists by checking for timeout. It uses a
// separate thread for command sending so we don't block the test harness.
//
// NOTE: We cannot call session.shutdown() after confirming a deadlock because
// shutdown() tries to join the processing thread (which is blocked on
// evt_tx.send()). Instead, we drain the event_rx to unblock the processing
// thread, then shut down normally.
// ============================================================================

#[test]
fn stress_annotation_deadlock_detection() {
    eprintln!("\n=== Test 13b: Annotation deadlock detection ===");
    eprintln!("  BUG: AnnotationAdded uses blocking send() -- testing for deadlock");
    let sample_rate = 2_048_000.0;
    let (path, _) = generate_test_cf32("annotdl", sample_rate, 5.0);

    let device = ReplayDevice::open(&path, sample_rate).expect("open replay device");
    let config = make_config(sample_rate, 2048);

    let (session, event_rx) =
        SessionManager::new_with_device(config, device, full_registry()).expect("start session");

    // Try to send 400 annotations WITHOUT draining events.
    // Command channel = 64 bounded, event channel = 256 bounded.
    // After ~256 annotations processed, event channel is full.
    // Processing thread blocks on send. Command channel fills to 64.
    // Total = ~320 before deadlock.
    let cmd_sender = session.cmd_sender();
    let sent_count = Arc::new(AtomicU64::new(0));
    let sent_clone = Arc::clone(&sent_count);
    let finished = Arc::new(AtomicBool::new(false));
    let finished_clone = Arc::clone(&finished);

    let sender_thread = std::thread::Builder::new()
        .name("annotation-sender".to_string())
        .spawn(move || {
            for i in 0..400u64 {
                if cmd_sender
                    .send(Command::AddAnnotation {
                        kind: "note".to_string(),
                        text: format!("annotation {i}"),
                    })
                    .is_err()
                {
                    break;
                }
                sent_clone.fetch_add(1, Ordering::Relaxed);
            }
            finished_clone.store(true, Ordering::Relaxed);
        })
        .expect("spawn sender");

    // Wait up to 5 seconds. If the sender doesn't finish, it's deadlocked.
    let deadline = Instant::now() + Duration::from_secs(5);
    while !finished.load(Ordering::Relaxed) && Instant::now() < deadline {
        std::thread::sleep(Duration::from_millis(50));
    }

    let sent = sent_count.load(Ordering::Relaxed);
    let did_finish = finished.load(Ordering::Relaxed);

    if !did_finish {
        eprintln!(
            "  CONFIRMED BUG: Deadlock detected! Only {} of 400 annotations sent before blocking.",
            sent
        );
        eprintln!("  Root cause: Event::AnnotationAdded uses blocking evt_tx.send()");
        eprintln!("  Fix: Use try_send_event() for AnnotationAdded, like high-frequency events");
    } else {
        eprintln!(
            "  Sent all {} annotations without deadlock (bug may be fixed)",
            sent
        );
    }

    // To cleanly shut down, we MUST drain the event channel first.
    // Otherwise the processing thread stays blocked on evt_tx.send() and
    // session.shutdown() will hang forever trying to join it.
    let stop_drain = Arc::new(AtomicBool::new(false));
    let (drain_handle, _) = spawn_event_drainer(event_rx, Arc::clone(&stop_drain));

    // Give the drainer a moment to unblock the processing thread
    std::thread::sleep(Duration::from_millis(200));

    // Now the sender thread should also unblock (commands can flow again)
    // Wait for it with a timeout
    let sender_deadline = Instant::now() + Duration::from_secs(3);
    loop {
        if finished.load(Ordering::Relaxed) || Instant::now() > sender_deadline {
            break;
        }
        std::thread::sleep(Duration::from_millis(50));
    }

    stop_drain.store(true, Ordering::Relaxed);
    session.shutdown();
    drain_handle.join().ok();
    sender_thread.join().ok();

    std::fs::remove_file(&path).ok();

    // This test documents the bug. We expect deadlock (sent < 400).
    // When the bug is fixed, all 400 should send successfully.
    if !did_finish {
        eprintln!(
            "  TEST RESULT: BUG CONFIRMED (deadlock at annotation {})",
            sent
        );
    } else {
        eprintln!("  TEST RESULT: No deadlock detected");
    }
}

