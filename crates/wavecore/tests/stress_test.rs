/// Generate a cf32 file containing a synthetic multi-tone signal.
/// Returns (path, sample_count).
fn generate_test_cf32(tag: &str, sample_rate: f64, duration_secs: f64) -> (PathBuf, usize) {
    let num_samples = (sample_rate * duration_secs) as usize;
    let path = PathBuf::from(format!(
        "/tmp/waverunner_stress_{tag}_{}.cf32",
        std::process::id()
    ));
    let mut file = std::fs::File::create(&path).expect("create test cf32 file");

    // Multi-tone signal: 3 tones at different offsets
    let freqs = [50_000.0f32, -75_000.0, 120_000.0];
    for i in 0..num_samples {
        let t = i as f32 / sample_rate as f32;
        let mut re = 0.0f32;
        let mut im = 0.0f32;
        for &f in &freqs {
            let phase = 2.0 * PI * f * t;
            re += phase.cos() * 0.1;
            im += phase.sin() * 0.1;
        }
        // Add some pseudo-noise
        let noise = (
            (i as f32 * 1.618033).sin() * 0.01,
            (i as f32 * std::f32::consts::E).cos() * 0.01,
        );
        re += noise.0;
        im += noise.1;
        file.write_all(&re.to_le_bytes()).unwrap();
        file.write_all(&im.to_le_bytes()).unwrap();
    }
    file.flush().unwrap();
    (path, num_samples)
}

/// Create a minimal session config.
fn make_config(sample_rate: f64, fft_size: usize) -> SessionConfig {
    SessionConfig {
        schema_version: 1,
        device_index: 0,
        frequency: 100e6,
        sample_rate,
        gain: GainMode::Auto,
        ppm: 0,
        fft_size,
        pfa: 1e-4,
    }
}

/// Create a fresh decoder registry with all decoders registered.
fn full_registry() -> DecoderRegistry {
    let mut reg = DecoderRegistry::new();
    decoders::register_all(&mut reg);
    reg
}

/// Drain events with timeout, returning final SessionStats.
fn drain_events(
    session: &SessionManager,
    event_rx: &crossbeam_channel::Receiver<Event>,
    timeout_secs: u64,
) -> Option<SessionStats> {
    let mut last_stats: Option<SessionStats> = None;
    let mut error_count = 0u64;
    let deadline = Instant::now() + Duration::from_secs(timeout_secs);

    loop {
        if Instant::now() > deadline {
            eprintln!("  [TIMEOUT] after {}s", timeout_secs);
            break;
        }
        match event_rx.recv_timeout(Duration::from_millis(300)) {
            Ok(Event::Stats(stats)) => {
                last_stats = Some(stats);
            }
            Ok(Event::Error(e)) => {
                error_count += 1;
                if error_count <= 5 {
                    eprintln!("  [ERROR] {}", e);
                }
            }
            Ok(_) => {}
            Err(crossbeam_channel::RecvTimeoutError::Timeout) => {
                if !session.is_running() {
                    break;
                }
            }
            Err(crossbeam_channel::RecvTimeoutError::Disconnected) => break,
        }
    }
    if error_count > 5 {
        eprintln!("  ... and {} more errors", error_count - 5);
    }
    last_stats
}

/// Spawn a background thread that continuously drains events from the receiver.
/// Returns a handle and an Arc containing the latest stats.
/// Set the stop flag to false to stop draining.
fn spawn_event_drainer(
    event_rx: crossbeam_channel::Receiver<Event>,
    stop: Arc<AtomicBool>,
) -> (
    std::thread::JoinHandle<()>,
    Arc<std::sync::Mutex<Option<SessionStats>>>,
) {
    let stats = Arc::new(std::sync::Mutex::new(None::<SessionStats>));
    let stats_clone = Arc::clone(&stats);
    let handle = std::thread::Builder::new()
        .name("event-drainer".to_string())
        .spawn(move || {
            while !stop.load(Ordering::Relaxed) {
                match event_rx.recv_timeout(Duration::from_millis(50)) {
                    Ok(Event::Stats(s)) => {
                        *stats_clone.lock().unwrap() = Some(s);
                    }
                    Ok(_) => {}
                    Err(_) => {}
                }
            }
            // Final drain after stop
            while let Ok(ev) = event_rx.try_recv() {
                if let Event::Stats(s) = ev {
                    *stats_clone.lock().unwrap() = Some(s);
                }
            }
        })
        .expect("spawn drainer thread");
    (handle, stats)
}

// ============================================================================
// Test 1: Rapid frequency hopping
// ============================================================================

#[test]
fn stress_rapid_frequency_hopping() {
    eprintln!("\n=== Test 1: Rapid frequency hopping ===");
    let sample_rate = 2_048_000.0;
    let (path, _) = generate_test_cf32("freqhop", sample_rate, 2.0);

    let device = ReplayDevice::open(&path, sample_rate).expect("open replay device");
    let config = make_config(sample_rate, 2048);

    let (session, event_rx) =
        SessionManager::new_with_device(config, device, full_registry()).expect("start session");

    // Drain events in background to prevent backpressure deadlock
    // (Tune commands produce Status events via blocking send)
    let stop = Arc::new(AtomicBool::new(false));
    let (drainer, drainer_stats) = spawn_event_drainer(event_rx, Arc::clone(&stop));

    // Slam 500 frequency changes in rapid succession
    for i in 0..500u64 {
        let freq = 88e6 + (i as f64 * 0.2e6);
        if session.send(Command::Tune(freq)).is_err() {
            eprintln!("  Command channel closed after {} tune commands", i);
            break;
        }
    }
    eprintln!("  Sent 500 tune commands");

    // Wait for session to finish
    let deadline = Instant::now() + Duration::from_secs(15);
    while session.is_running() && Instant::now() < deadline {
        std::thread::sleep(Duration::from_millis(100));
    }

    stop.store(true, Ordering::Relaxed);
    session.shutdown();
    drainer.join().expect("drainer panicked");
    std::fs::remove_file(&path).ok();

    let stats = drainer_stats.lock().unwrap().clone();
    if let Some(stats) = stats {
        eprintln!(
            "  blocks_processed={}, blocks_dropped={}, events_dropped={}",
            stats.blocks_processed, stats.blocks_dropped, stats.events_dropped
        );
        assert!(
            stats.blocks_processed >= 1,
            "Expected at least 1 block processed"
        );
    }
    eprintln!("  PASSED: No panic, no deadlock");
}

